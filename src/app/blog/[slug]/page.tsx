// 根据slug生成元数据
export async function generateMetadata({ params }: { params: { slug: string } }): Promise<Metadata> {
  const article = articlesData[params.slug];
  
  if (!article) {
    return {
      title: '文章未找到 | 易经环境设计',
      description: '抱歉，您请求的文章不存在。'
    };
  }
  
  return {
    title: `${article.title} | 易经环境设计`,
    description: article.summary,
  };
}

